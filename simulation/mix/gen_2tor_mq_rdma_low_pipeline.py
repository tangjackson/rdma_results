#!/usr/bin/env python3
"""
Generate the MQ-RDMA variant of the low-pipeline hotspot experiment.

MQ-RDMA isolates traffic classes by assigning them to different RDMA priority
groups / switch queues while keeping PFC enabled:
- pipeline victim traffic: PG 4
- hotspot all-to-all trigger traffic: PG 3

The same-queue comparison for this experiment is:
  gen_2tor_alltoall_pfc_trigger_low_pipeline.py

Both use the same low pipeline load and bursty all-to-all shape. The expected
benefit of MQ-RDMA is that PFC pauses generated by all-to-all queue pressure
apply to PG 3 and do not directly pause the pipeline's PG 4 queue.
"""

from __future__ import annotations

import subprocess
import sys
from pathlib import Path


def main() -> int:
    mix_dir = Path(__file__).resolve().parent
    generator = mix_dir / "gen_2tor_pfc_hotspot.py"
    cmd = [
        sys.executable,
        str(generator),
        "--prefix",
        "2tor_mq_rdma_low_pipeline",
        "--enable-trace",
        "--link-rate-gbps",
        "100",
        "--pipeline-nodes-per-rack",
        "16",
        "--pipeline-ring-layout",
        "rack_contiguous",
        "--pipeline-rounds",
        "8",
        "--pipeline-flow-bytes",
        str(256 * 1024),
        "--pipeline-gap-us",
        "250",
        "--pipeline-base-us",
        "0",
        "--pipeline-pg",
        "4",
        "--alltoall-node-list",
        "24-31",
        "--alltoall-flow-bytes",
        str(256 * 1024),
        "--alltoall-rounds",
        "8",
        "--alltoall-round-gap-us",
        "20",
        "--alltoall-base-us",
        "500",
        "--alltoall-src-stagger-us",
        "0",
        "--alltoall-pg",
        "3",
        "--pfc-xoff-bytes",
        "32768",
        "--pfc-xon-bytes",
        "16384",
    ]
    cmd.extend(sys.argv[1:])
    return subprocess.run(cmd, cwd=str(mix_dir.parent)).returncode


if __name__ == "__main__":
    raise SystemExit(main())
